-- Magnitude Analysis

-- Find total customers by countries

SELECT 
	country, 
	COUNT(customer_key) AS total_customers 
FROM gold.dim_customers
GROUP BY country
ORDER BY total_customers DESC

-- Find total customers by genders

SELECT 
	gender, 
	COUNT(customer_key) AS total_customers 
FROM gold.dim_customers 
GROUP BY gender
ORDER BY total_customers DESC

-- Find total products by category

SELECT 
	category, 
	COUNT(category) AS total_products 
FROM gold.dim_products 
GROUP BY category
ORDER BY total_products DESC

-- What is the average cost in each category?

SELECT 
	category, 
	AVG(cost) AS avg_costs 
FROM gold.dim_products 
GROUP BY category
ORDER BY avg_costs DESC

-- What is the total revenue generated for each category?

SELECT 
	p.category,
	SUM(f.sales_amount)  AS total_revenue
FROM gold.fact_sales f 
LEFT JOIN gold.dim_products p ON p.product_key = f.product_key
GROUP BY p.category
ORDER BY total_revenue DESC


-- Find total revenue is generated by each customer

SELECT 
	c.customer_key,
	c.first_name,
	c.last_name,
	SUM(f.sales_amount) AS total_revenue
FROM gold.fact_sales f
LEFT JOIN gold.dim_customers c ON c.customer_key = f.customer_key
GROUP BY c.customer_key, c.first_name, c.last_name
ORDER BY total_revenue DESC

-- What is the distribution of sold items across countries?

SELECT 
	c.country,
	SUM(f.quantity) AS total_sold_items
FROM gold.fact_sales f
LEFT JOIN gold.dim_customers c ON c.customer_key = f.customer_key
GROUP BY c.country
ORDER BY total_sold_items DESC


-- Ranking Analysis

-- Which 5 products generate the highest revenue? 

SELECT TOP 5
	p.product_name,
	SUM(f.sales_amount)  AS total_revenue
FROM gold.fact_sales f 
LEFT JOIN gold.dim_products p ON p.product_key = f.product_key
GROUP BY p.product_name
ORDER BY total_revenue DESC

SELECT *
FROM (
	SELECT
		p.product_name,
		SUM(f.sales_amount)  AS total_revenue,
		ROW_NUMBER() OVER (ORDER BY SUM(f.sales_amount) DESC) AS rank_products
	FROM gold.fact_sales f 
	LEFT JOIN gold.dim_products p ON p.product_key = f.product_key
	GROUP BY p.product_name ) AS t
WHERE rank_products <= 5



-- What are the 5 worst-performing products in terms of sales?

SELECT TOP 5
	p.product_name,
	SUM(f.sales_amount)  AS total_revenue
FROM gold.fact_sales f 
LEFT JOIN gold.dim_products p ON p.product_key = f.product_key
GROUP BY p.product_name
ORDER BY total_revenue ASC

SELECT * 
FROM (
	SELECT 
		p.product_name,
		SUM(f.sales_amount)  AS total_revenue,
		RANK() OVER (ORDER BY SUM(f.sales_amount) ASC) AS rank_products
	FROM gold.fact_sales f 
	LEFT JOIN gold.dim_products p ON p.product_key = f.product_key
	GROUP BY p.product_name) AS t
WHERE rank_products <= 5

-- Find the top 10 customers whý have generated the highest revenue
SELECT *
FROM (
	SELECT 
		c.customer_key,
		c.first_name,
		c.last_name,
		SUM(f.sales_amount) AS total_revenue,
		ROW_NUMBER() OVER (ORDER BY SUM(f.sales_amount) DESC) AS rank_customers
	FROM gold.fact_sales f
	LEFT JOIN gold.dim_customers c ON c.customer_key = f.customer_key
	GROUP BY c.customer_key, c.first_name, c.last_name ) AS t
WHERE rank_customers <= 10

-- The 3 customers with the fewest orders placed

